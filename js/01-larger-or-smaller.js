var number1 = parseInt(window.prompt("Enter the first number"));
var number2 = parseInt(window.prompt("Enter the second number"));

if (number1 > number2) {
    window.document.write("The first number " + number1 + " is greater than the second number " + number2);
}else if (number1 < number1) {
    window.document.write("The first number " + number2 + " is greater than the second number " + number1);
}else {
    window.document.write("The numbers are equal.")
}